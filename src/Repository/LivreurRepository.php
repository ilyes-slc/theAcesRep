    public function findEntitiesByString($str){
        return $this->getEntityManager()
            ->createQuery(
                'SELECT e
                FROM App:Livreur e
                WHERE e.prenom LIKE :str
                OR e.cin LIKE :str 
                OR e.name LIKE :str 
                OR e.rating LIKE :str
                OR e.dateNaissance LIKE :str
                OR e.zone LIKE :str
                OR e.tel LIKE :str'
            )
            ->setParameter('str', '%'.$str.'%')
            ->getResult();
    }
